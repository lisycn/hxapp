+ (NSString*)encrypt:(NSString*)plainText withKey:(NSString*)key{
    NSString *keyValue = key;
    int len = key.length;
    if (32 == len) {
        keyValue = [key stringByAppendingString:[key substringToIndex:16]];
    }
    NSData *keyData = [NSString stringToByte:keyValue];
    
    NSData *data = [NSString stringToByte:plainText];
    
    size_t plainTextBufferSize = [data length];
    const void *vplainText = (const void *)[data bytes];
    
    CCCryptorStatus ccStatus;
    uint8_t *bufferPtr = NULL;
    size_t bufferPtrSize = 0;
    size_t movedBytes = 0;
    
    bufferPtrSize = (plainTextBufferSize + kCCBlockSize3DES) & ~(kCCBlockSize3DES - 1);
    bufferPtr = malloc( bufferPtrSize * sizeof(uint8_t));
    memset((void *)bufferPtr, 0x0, bufferPtrSize);
    
    const void *vkey = (const void *) keyData.bytes;
    const void *vinitVec = (const void *) [gIv UTF8String];
    
    ccStatus = CCCrypt(kCCEncrypt,
                       kCCAlgorithm3DES,
                       kCCOptionECBMode,
                       vkey,
                       kCCKeySize3DES,
                       vinitVec,
                       vplainText,
                       plainTextBufferSize,
                       (void *)bufferPtr,
                       bufferPtrSize,
                       &movedBytes);
    
    NSData *myData = [NSData dataWithBytes:(const void *)bufferPtr length:(NSUInteger)movedBytes];
    
    const char * r = [myData bytes];
    NSString *result = [CJCommon bytesToHexString:(char *)r len:myData.length];
    return [result uppercaseString];
}

